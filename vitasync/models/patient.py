"""
@file patient.py

@brief Defines the patient models used throughout the VitaSync
application.

@details
Provides the primary patient models responsible for representing patient
information within the VitaSync system.

This module includes:

- the MedicalCondition enumeration, representing the supported clinical
  conditions that may be associated with a patient;
- the Patient model, which stores complete patient information;
- the PatientCreate model, which represents the information required when
  creating a new patient record.

These models perform validation and normalization of patient data,
maintain consistency with associated ABHA KYC information where
available, and ensure that all patient records satisfy the application's
business rules before being persisted.
"""

from datetime import datetime, date
import re
from enum import StrEnum, auto
from pydantic import (
    BaseModel,
    Field,
    field_validator,
    model_validator,
    ConfigDict
)

from vitasync.models.ABHA.kyc import ABHAKYC
from vitasync.common.converter import concatenate_name
from vitasync.common.idgenerator import PatientID
import vitasync.common.validator as validator


